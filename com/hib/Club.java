package com.hib;

public class Club {
	private int id;
	private String name;
	
	public Club() {
		super();
	}
	
	public Club(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
